function addIndex(user, index) {
  let indexGroup
  if (index <= 9) {
    indexGroup = 'top-10'
  } else if (index <= 49) {
    indexGroup = 'top-50'
  } else if (index <= 79) {
    indexGroup = 'top-80'
  } else if (index <= 99) {
    indexGroup = 'top-100'
  }

  const result = user
  result.group.push(indexGroup, 'top')
  result.index = index
  result.name = [result.id, result.name].join(' :: ')

  return result
}

